    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // Botão Obter nova fila:
        Queue novafila = criaFilaLivrosPosterioresA2002(fila); //envia a 'fila' que é um atributo desta classe
        jTextArea1.setText("Fila original sem alterar:\n" + fila.toString()
                           + "\n\nFila de livros com ano > 2002:\n" +novafila.toString());
    }//GEN-LAST:event_jButton4ActionPerformed

    public Queue criaFilaLivrosPosterioresA2002(Queue fila) {
        Queue fila2002 = new Queue();  // fila de livros publicados depois de 2002
        Queue filatemp = new Queue();  //fila auxiliar
        while(!fila.isEmpty()) {
            Livro liv = (Livro)fila.peek();
            if(liv.getAnopublic() > 2002) fila2002.enqueue(liv);
            filatemp.enqueue(fila.dequeue());  //adiciono na fila auxiliar todos os livros da fila original
        }
        while(!filatemp.isEmpty()) {
            fila.enqueue(filatemp.dequeue());
        }
        return fila2002;
    }
    
