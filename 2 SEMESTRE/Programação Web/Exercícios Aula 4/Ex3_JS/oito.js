// 8 -  Faça um script para calcular e mostrar a área de um triângulo (área = (base * altura) /2

"use strict"

let area;
alert("Calculpo de área do triangulo.")
let base = parseInt(prompt("Digite o valor da base: "));
let altura = parseInt(prompt("Digite o valor da altura: "));

area = (base * altura) /2;

alert("O valor da área do triangulo é: " + area);